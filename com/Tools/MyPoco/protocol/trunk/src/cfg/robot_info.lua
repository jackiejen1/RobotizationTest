local m = { 
    __index = function(t, k) 
        if k == "toObject" then
            return function()  
                local o = {}
                for key, v in pairs (__key_map) do 
                    o[key] = t._raw[v]
                end
                return o
            end 
        end
        
        assert(__key_map[k], "cannot find " .. k .. " in record_robot_info")

        return t._raw[__key_map[k]]
    end
}


function robot_info.getLength()
    return #robot_info._data
end



function robot_info.hasKey(k)
  if __key_map[k] == nil then
    return false
  else
    return true
  end
end


---
--@return @class record_robot_info
function robot_info.indexOf(index)
    if index == nil or not robot_info._data[index] then
        return nil
    end
    return setmetatable({_raw = robot_info._data[index]}, m)
end



---
--@return @class record_robot_info
function robot_info.get(id)
    
    return robot_info.indexOf(__index_id[ id ])
     
end



function robot_info.set(id, key, value)
