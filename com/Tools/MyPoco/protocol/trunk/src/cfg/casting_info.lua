local m = { 
    __index = function(t, k) 
        if k == "toObject" then
            return function()  
                local o = {}
                for key, v in pairs (__key_map) do 
                    o[key] = t._raw[v]
                end
                return o
            end 
        end
        
        assert(__key_map[k], "cannot find " .. k .. " in record_casting_info")

        return t._raw[__key_map[k]]
    end
}


function casting_info.getLength()
    return #casting_info._data
end



function casting_info.hasKey(k)
  if __key_map[k] == nil then
    return false
  else
    return true
  end
end


---
--@return @class record_casting_info
function casting_info.indexOf(index)
    if index == nil or not casting_info._data[index] then
        return nil
    end
    return setmetatable({_raw = casting_info._data[index]}, m)
end



