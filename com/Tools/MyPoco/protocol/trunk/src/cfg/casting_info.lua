    if index == nil or not casting_info._data[index] then
        return nil
    end
    return setmetatable({_raw = casting_info._data[index]}, m)
end



---
--@return @class record_casting_info
function casting_info.get(id,level)
    
    local k = id .. '_' .. level
    return casting_info.indexOf(__index_id_level[k])
     
end



function casting_info.set(id,level, key, value)
    local record = casting_info.get(id,level)
    if record then
        local keyIndex = __key_map[key]
        if keyIndex then
            record._raw[keyIndex] = value
        end
    end
end




function casting_info.get_index_data()
    return __index_id_level 
end

return  casting_info 