local m = { 
    __index = function(t, k) 
        if k == "toObject" then
            return function()  
                local o = {}
                for key, v in pairs (__key_map) do 
                    o[key] = t._raw[v]
                end
                return o
            end 
        end
        
        assert(__key_map[k], "cannot find " .. k .. " in record_achievement_info")

        return t._raw[__key_map[k]]
    end
}


function achievement_info.getLength()
    return #achievement_info._data
end



function achievement_info.hasKey(k)
  if __key_map[k] == nil then
    return false
  else
    return true
  end
end


---
--@return @class record_achievement_info
function achievement_info.indexOf(index)
    if index == nil or not achievement_info._data[index] then
        return nil
    end
    return setmetatable({_raw = achievement_info._data[index]}, m)
end



---
--@return @class record_achievement_info
function achievement_info.get(id)
    
    return achievement_info.indexOf(__index_id[ id ])
     
end



function achievement_info.set(id, key, value)
    local record = achievement_info.get(id)
    if record then
        local keyIndex = __key_map[key]
        if keyIndex then
            record._raw[keyIndex] = value
        end
    end
end




