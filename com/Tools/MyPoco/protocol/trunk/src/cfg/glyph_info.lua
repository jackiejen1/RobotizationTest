local m = { 
    __index = function(t, k) 
        if k == "toObject" then
            return function()  
                local o = {}
                for key, v in pairs (__key_map) do 
                    o[key] = t._raw[v]
                end
                return o
            end 
        end
        
        assert(__key_map[k], "cannot find " .. k .. " in record_glyph_info")

        return t._raw[__key_map[k]]
    end
}


function glyph_info.getLength()
    return #glyph_info._data
end



function glyph_info.hasKey(k)
  if __key_map[k] == nil then
    return false
  else
    return true
  end
end


---
--@return @class record_glyph_info
function glyph_info.indexOf(index)
    if index == nil or not glyph_info._data[index] then
        return nil
    end
    return setmetatable({_raw = glyph_info._data[index]}, m)
end



---
--@return @class record_glyph_info
function glyph_info.get(id,level)
    
    local k = id .. '_' .. level
    return glyph_info.indexOf(__index_id_level[k])
     
end



function glyph_info.set(id,level, key, value)
    local record = glyph_info.get(id,level)
    if record then
        local keyIndex = __key_map[key]
        if keyIndex then
            record._raw[keyIndex] = value
        end
    end
end




function glyph_info.get_index_data()
    return __index_id_level 
end

