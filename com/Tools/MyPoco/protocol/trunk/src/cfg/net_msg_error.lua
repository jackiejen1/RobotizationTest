local m = { 
    __index = function(t, k) 
        if k == "toObject" then
            return function()  
                local o = {}
                for key, v in pairs (__key_map) do 
                    o[key] = t._raw[v]
                end
                return o
            end 
        end
        
        assert(__key_map[k], "cannot find " .. k .. " in record_net_msg_error")

        return t._raw[__key_map[k]]
    end
}


function net_msg_error.getLength()
    return #net_msg_error._data
end



function net_msg_error.hasKey(k)
  if __key_map[k] == nil then
    return false
  else
    return true
  end
end


