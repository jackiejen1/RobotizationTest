    return biography_battle_info.indexOf(__index_id[ id ])
     
end



function biography_battle_info.set(id, key, value)
    local record = biography_battle_info.get(id)
    if record then
        local keyIndex = __key_map[key]
        if keyIndex then
            record._raw[keyIndex] = value
        end
    end
end




function biography_battle_info.get_index_data()
    return __index_id 
end

return  biography_battle_info 